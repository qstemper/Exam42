#include <unistd.h>

int main(int ac, char **av)
{
	char	*s1;
	char	*s2;

	if (ac != 3)
	{
		write(1, "\n", 1);
		return (1);
	}
	s1 = av[1];
	s2 = av[2];
	if (*s1 == '\0')
	{
		write(1, "1\n", 2);
		return (0);
	}
	while (*s1 && *s2)
	{
		if (*s1 == *s2)
			s1++;
		s2++;
	}
	write(1, (*s1 ? "0\n" : "1\n"), 2);
	return (0);
}
